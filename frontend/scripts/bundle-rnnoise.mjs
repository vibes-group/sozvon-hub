#!/usr/bin/env node
// Bundles the Shiguredo RNNoise vendor + our processor into one
// self-contained AudioWorklet file with zero ESM imports. Output:
// public/vendor/rnnoise/worklet.js (committed). Run via
// `npm run bundle:rnnoise` whenever the vendor or processor source
// changes.
//
// Vendor WASM lives in public/vendor/rnnoise/rnnoise.js (shared with the
// legacy engine, which fetches it at runtime). Output `worklet.js` lands in
// the same directory and is self-contained — no runtime dependency on the
// vendor file beyond build time.
//
// Why bundle: AudioWorkletGlobalScope in WebView2 rejects `import` and
// `import.meta`, so the vendor must be inlined and stripped of ESM
// constructs. Doing it once at build time is the Jitsi-style pattern.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');

const vendorPath = resolve(root, 'public/vendor/rnnoise/rnnoise.js');
const processorPath = resolve(root, 'src/audio/rnnoise.worklet.src.js');
const outPath = resolve(root, 'public/vendor/rnnoise/worklet.js');

const vendor = readFileSync(vendorPath, 'utf8');
const processor = readFileSync(processorPath, 'utf8');

const exportMatch = vendor.match(/export\s*\{([^}]*)\}\s*;?\s*$/);
if (!exportMatch) throw new Error('vendor: trailing ESM export not found');
// Sanity: the match must actually be at the file tail. A future vendor build
// could emit `export {}` mid-file; slice(0, index) would silently truncate.
if (exportMatch.index < vendor.length - 200) {
  throw new Error(
    `vendor: ESM export found at offset ${exportMatch.index} but file is ${vendor.length} bytes — not trailing`,
  );
}
const aliasMatch = /(\w+)\s+as\s+Rnnoise/.exec(exportMatch[1]);
if (!aliasMatch) throw new Error('vendor: `Rnnoise` alias not found in export');
const localId = aliasMatch[1];

const vendorClean = vendor
  .slice(0, exportMatch.index)
  .replace(/import\.meta\.url/g, '""')
  + `\nvar Rnnoise = ${localId};\n`;

// `WorkerGlobalScope` stub: vendor's env check is `typeof WorkerGlobalScope < "u"`.
// Worklet scope has neither `window` nor `WorkerGlobalScope`, so the check would
// throw. The vendor never invokes or inspects the value — only the typeof matters.
const preamble = `// AUTO-GENERATED by scripts/bundle-rnnoise.mjs — do not edit.
// Source: public/vendor/rnnoise/rnnoise.js + src/audio/rnnoise.worklet.src.js
var WorkerGlobalScope = function () {};
`;

const out = preamble + vendorClean + '\n' + processor + '\n';
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, out);
console.log(`wrote ${outPath} (${out.length} bytes)`);
